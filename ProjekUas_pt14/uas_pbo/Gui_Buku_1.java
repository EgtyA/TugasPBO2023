        b.setKategori(kategori);
        b.setPengarang(pengarang);
        b.setPenerbit(penerbit);
        b.setTahunTerbit(tahun_terbit);
        b.setJumlahHalaman(jumlahHalaman);

        em.getTransaction().begin();
